def normalize(s):
    if not s:
        return ''
    for radical, std in RADICAL_MAP.items():
        s = s.replace(radical, std)
    s = re.sub(r'\s+', '', s)
    s = s.replace('?', '?').replace(',', ',').replace('。', '.')
    s = s.replace('(', '(').replace(')', ')').replace('!', '!')
    return s.lower()


def fingerprint(stem, answer):
    return hashlib.md5(f"{normalize(stem)[:30]}|{answer}".encode('utf-8')).hexdigest()


