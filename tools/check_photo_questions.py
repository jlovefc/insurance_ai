def main():
    conn = sqlite3.connect(DB)
    cur = conn.cursor()

    # 建立 DB 指紋表(stem 前 30 字 + answer)
    fps_in_db = {}
    for qid, content, ans, subj in cur.execute(
        "SELECT id, content, correct_answer, subject FROM questions"
    ):
        if not content or not ans:
            continue
        fp = fingerprint(content, ans)
        fps_in_db[fp] = (qid, subj, content[:40])

    print(f"DB 現有指紋: {len(fps_in_db)} 個\n")
    print(f"{'='*80}")
    print(f" 比對結果")
    print(f"{'='*80}\n")

    new_count = 0
    dup_count = 0
    new_questions = []
    dup_questions = []

    for label, stem, ans in QUESTIONS_TO_CHECK:
        fp = fingerprint(stem, ans)
        if fp in fps_in_db:
            qid, subj, db_stem = fps_in_db[fp]
            print(f"  ❌ {label} 重複 → DB #{qid} [{subj}]: {db_stem}")
            dup_count += 1
            dup_questions.append(label)
        else:
            print(f"  ✅ {label} 新題 → {stem}")
            new_count += 1
            new_questions.append(label)

    print(f"\n{'='*80}")
    print(f" 總結")
    print(f"{'='*80}")
    print(f"  測試題數: {len(QUESTIONS_TO_CHECK)}")
    print(f"  ✅ 新題: {new_count}")
    print(f"  ❌ 重複: {dup_count}")
    if new_questions:
        print(f"\n  新題清單: {', '.join(new_questions)}")

    conn.close()


if __name__ == "__main__":
    main()
